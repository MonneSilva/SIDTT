    {
        \SIDTT\usu::create([
        'nombre' => $request['nom'].' '.$request['aP'].' '.$request['aM'],
        'email' => $request['email'],
        'boleta' => $request['boleta'],
        'pass' => bcrypt($request['pass']),
        ]);
        
        return view('alumno.registro');
    }

